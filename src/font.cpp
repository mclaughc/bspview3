#pragma pack(push, 1)
struct FontVertex
{
  float pos[2];
  float tex[2];
  u32 col;

  void Set(float x, float y, float u, float v, u32 color)
  {
    pos[0] = x;
    pos[1] = y;
    tex[0] = u;
    tex[1] = v;
    col = color;
  }
};
#pragma pack(pop)

static const char s_vertex_shader[] = R"(
#version %d

in vec2 in_position;
in vec2 in_tex0;
in vec4 in_color;

out vec2 v_tex;
out vec4 v_col;

void main()
{
  gl_Position = vec4(in_position, 0.0, 1.0);
  v_tex = in_tex0;
  v_col = in_color;
}
)";

static const char s_fragment_shader[] = R"(
#version %d

uniform sampler2D samp0;

in vec2 v_tex;
in vec4 v_col;

out vec4 ocol0;

void main()
{
  ocol0 = texture(samp0, v_tex).rrrr * v_col;
}
)";

static const VertexAttribute s_hud_vertex_attributes[] = {
  {"in_position", GL_FLOAT, 2, 0, offsetof(FontVertex, pos), sizeof(FontVertex), false},
  {"in_tex0", GL_FLOAT, 2, 0, offsetof(FontVertex, tex), sizeof(FontVertex), false},
  {"in_color", GL_UNSIGNED_BYTE, 4, 0, offsetof(FontVertex, col), sizeof(FontVertex), true}};

Font::Font(std::unique_ptr<ShaderProgram> program, std::unique_ptr<Texture> texture, std::unique_ptr<Buffer> buffer,
           std::unique_ptr<VertexArray> vertex_array)
  : m_program(std::move(program)), m_texture(std::move(texture)), m_vertex_buffer(std::move(buffer)),
    m_vertex_array(std::move(vertex_array))
{
  FillGlyphInfo();
}

Font::~Font() {}

void Font::RenderText(s32 x, s32 y, u32 viewport_width, u32 viewport_height, u32 color, const char* text)
{
  const float rcp_viewport_width = 1.0f / static_cast<float>(viewport_width);
  const float rcp_viewport_height = 1.0f / static_cast<float>(viewport_height);
  s32 current_x = x;
  s32 current_y = y;

  m_vertex_array->Bind();
  m_program->Bind();
  m_texture->Bind(0);

  glDisable(GL_DEPTH_TEST);
  glEnable(GL_BLEND);
  glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);

  size_t remaining_chars = std::strlen(text);
  while (remaining_chars > 0)
  {
    FontVertex* vout = reinterpret_cast<FontVertex*>(m_vertex_buffer->Map(false, true));
    if (!vout)
      return;

    size_t num_vertices = 0;
    while (remaining_chars > 0 && num_vertices < MAX_VERTICES_PER_DRAW)
    {
      const GlyphInfo& gi = m_glyphs[*(text++)];
      if (gi.skip)
      {
        current_x += gi.xadvance;
        remaining_chars--;
        continue;
      }

      const float x0 = (float(current_x + gi.x0) * rcp_viewport_width) * 2.0f - 1.0f;
      const float x1 = (float(current_x + gi.x1) * rcp_viewport_width) * 2.0f - 1.0f;
      const float y0 = (float(current_y + gi.y0) * rcp_viewport_height) * -2.0f + 1.0f;
      const float y1 = (float(current_y + gi.y1) * rcp_viewport_height) * -2.0f + 1.0f;

      (vout++)->Set(x0, y0, gi.u0, gi.v0, color);
      (vout++)->Set(x1, y0, gi.u1, gi.v0, color);
      (vout++)->Set(x0, y1, gi.u0, gi.v1, color);
      (vout++)->Set(x0, y1, gi.u0, gi.v1, color);
      (vout++)->Set(x1, y0, gi.u1, gi.v0, color);
      (vout++)->Set(x1, y1, gi.u1, gi.v1, color);
      num_vertices += 6;
      current_x += gi.xadvance;
      remaining_chars--;
    }

    m_vertex_buffer->Unmap();
    if (num_vertices > 0)
    {
      glDrawArrays(GL_TRIANGLES, 0, num_vertices);
      g_statistics->AddDraw();
    }
  }

  glDisable(GL_BLEND);
}

void Font::RenderFormattedText(s32 x, s32 y, u32 viewport_width, u32 viewport_height, u32 color, const char* format,
                               ...)
{
  va_list ap;
  va_start(ap, format);
  std::string str = Util::StringFromFormatV(format, ap);
  va_end(ap);

  return RenderText(x, y, viewport_width, viewport_height, color, str.c_str());
}

std::unique_ptr<Font> Font::Create()
{
  std::vector<u8> texture_data(FONT_TEXTURE_WIDTH * FONT_TEXTURE_HEIGHT);
  size_t opos = 0;
  for (size_t ipos = 0; ipos < ARRAY_SIZE(s_font_texture_bits); ipos++)
  {
    const u8 val = s_font_texture_bits[ipos];
    texture_data[opos++] = (val & 0x01) ? 0xFF : 0x00;
    texture_data[opos++] = (val & 0x02) ? 0xFF : 0x00;
    texture_data[opos++] = (val & 0x04) ? 0xFF : 0x00;
    texture_data[opos++] = (val & 0x08) ? 0xFF : 0x00;
    texture_data[opos++] = (val & 0x10) ? 0xFF : 0x00;
    texture_data[opos++] = (val & 0x20) ? 0xFF : 0x00;
    texture_data[opos++] = (val & 0x40) ? 0xFF : 0x00;
    texture_data[opos++] = (val & 0x80) ? 0xFF : 0x00;
  }

  std::unique_ptr<Texture> texture = Texture::Create(Texture::Format::FORMAT_R8, FONT_TEXTURE_WIDTH,
                                                     FONT_TEXTURE_HEIGHT, 1, texture_data.data(), false, false, false);
  if (!texture)
    return nullptr;

  std::string vertex_shader_str = Util::StringFromFormat(s_vertex_shader, 330);
  std::string fragment_shader_str = Util::StringFromFormat(s_fragment_shader, 330);
  std::unique_ptr<Shader> vertex_shader =
    Shader::Create(GL_VERTEX_SHADER, vertex_shader_str.c_str(), vertex_shader_str.length());
  std::unique_ptr<Shader> fragment_shader =
    Shader::Create(GL_FRAGMENT_SHADER, fragment_shader_str.c_str(), fragment_shader_str.length());
  if (!vertex_shader || !fragment_shader)
    return nullptr;

  std::unique_ptr<ShaderProgram> program = ShaderProgram::Create(
    s_hud_vertex_attributes, ARRAY_SIZE(s_hud_vertex_attributes), vertex_shader.get(), fragment_shader.get(), 1);
  if (!program)
    return nullptr;

  std::unique_ptr<Buffer> buffer = Buffer::Create(Buffer::Type::VertexBuffer, VERTEX_BUFFER_SIZE, nullptr, true);
  if (!buffer)
    return nullptr;

  const Buffer* vao_buffers[1] = {buffer.get()};
  std::unique_ptr<VertexArray> vertex_array =
    VertexArray::Create(s_hud_vertex_attributes, ARRAY_SIZE(s_hud_vertex_attributes), vao_buffers);
  if (!vertex_array)
    return nullptr;

  return std::unique_ptr<Font>(
    new Font(std::move(program), std::move(texture), std::move(buffer), std::move(vertex_array)));
}

void Font::FillGlyphInfo()
{
  const float rcp_width = 1.0f / static_cast<float>(m_texture->GetWidth());
  const float rcp_height = 1.0f / static_cast<float>(m_texture->GetHeight());

  for (size_t i = 0; i < NUM_CHARS; i++)
  {
    const StaticGlyphInfo& sgi = s_glyph_info[i];
    GlyphInfo& dgi = m_glyphs[i];

    dgi.u0 = static_cast<float>(sgi.xpos) * rcp_width;
    dgi.v0 = static_cast<float>(sgi.ypos) * rcp_height;
    dgi.u1 = static_cast<float>(sgi.xpos + sgi.width) * rcp_width;
    dgi.v1 = static_cast<float>(sgi.ypos + sgi.height) * rcp_height;
    dgi.x0 = sgi.xoffset;
    dgi.x1 = sgi.xoffset + sgi.width;
    dgi.y0 = sgi.yoffset;
    dgi.y1 = sgi.yoffset + sgi.height;
    dgi.xadvance = sgi.xadvance;
    dgi.yadvance = sgi.yadvance;
    dgi.skip = (sgi.width == 0 || sgi.height == 0);
  }
}
